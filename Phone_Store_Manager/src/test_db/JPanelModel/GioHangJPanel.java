    public void tongtien(List<GioHang> listGH) {
        double total = 0;
        for (int i = 0; i < listGH.size(); i++) {
            total = total + (Double.parseDouble(listGH.get(i).getDonGiaSP()) * listGH.get(i).getSlSP());
        }
        jlbltongtien.setText(String.valueOf(total));
        jlblthanhtoan.setText(String.valueOf(total));
    }
    
    private void btnxoaspActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaspActionPerformed
        if (listGH.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa có sản phẩm trong giỏ hàng !");
        } else {
            int choice = JOptionPane.showConfirmDialog(btnxoasp, "Bạn có chắc chắn xóa sản phẩm này ?", "",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (choice == 0) {
                inDex = qlghc.getIndex();
                System.out.println(inDex);
                for (int i = 0; i < listSP.size(); i++) {
                    if (listGH.get(inDex).getmSP() == listSP.get(i).getMaSP()) {
                        System.out.println("delete = " + i);
                        idCheck[i] = 0;
                    }
                }
                listGH.remove(inDex);
                qlghc.setDataModel(listGH);
                tongtien(listGH);
            }
        }
    }//GEN-LAST:event_btnxoaspActionPerformed

