    private void btnthemspActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemspActionPerformed
        idRow = controller.getIndex();
        GioHang gh = new GioHang();
        if (idRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm ");
        } else {
            if (idCheck[idRow] == 0) {
//                List<SanPham> listSP = iSanPhamService.listSPService();
                gh.setmSP(listSP.get(idRow).getMaSP());
                gh.settSP(listSP.get(idRow).getTenSP());
                gh.setDonGiaSP(listSP.get(idRow).getGiaSP());
                listGH.add(gh);
                GioHangJFrameAddSP frameAddSP = new GioHangJFrameAddSP(gh, listGH, jpngiohang, jlbltongtien, jlblthanhtoan);
                frameAddSP.setResizable(false);
                frameAddSP.setLocationRelativeTo(null);
                frameAddSP.setVisible(true);
                frameAddSP.setIndex(idRow);
                idCheck[idRow] = 1;
            } else {
                JOptionPane.showMessageDialog(this, "Sản phẩm đã được thêm");
            }
        }
    }//GEN-LAST:event_btnthemspActionPerformed

