    public void UpdateSL(int sl){
        try {
            Connection cons = (Connection) dbConnect.getConnection();
            int value = getIndex() + 1;
            String sql = "Update SANPHAM set SOLUONG = ? where MASANPHAM = " + value;
            PreparedStatement ps = cons.prepareStatement(sql, PreparedStatement.RETURN_GENERATED_KEYS);

            System.out.println(Integer.valueOf(iSanPhamService.listSPService().get(getIndex()).getSoLuongSP()) - sl);
            ps.setString(1, String.valueOf(Integer.valueOf(iSanPhamService.listSPService().get(getIndex()).getSoLuongSP()) - sl));
            
            ps.executeUpdate();
            ps.close();
            
            cons.close();
            JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công!");
            dispose();
        } catch (SQLException ex) {
            Logger.getLogger(KhachHangJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

