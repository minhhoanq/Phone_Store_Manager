    public void addSLSP(GioHang gh, List<GioHang> list){
        btnoksl.addMouseListener(new MouseAdapter(){
            @Override
            public void mouseClicked(MouseEvent e) {
                int sl = Integer.parseInt(jtfslsp.getText());
                gh.setSlSP(sl);
                System.out.println(gh.toString());
                UpdateSL(sl);
                QLGHController qlghc = new QLGHController(jpntable);
                qlghc.setDataModel(list);
                tongtien(list);
                dispose();
            }
        });
    }
 
    public void tongtien(List<GioHang> listGH) {
        double total = 0;
        for (int i = 0; i < listGH.size(); i++) {
            total = total + (Double.parseDouble(listGH.get(i).getDonGiaSP()) * listGH.get(i).getSlSP());
        }
        jlbltongtien.setText(String.valueOf(total));
        jlblthanhtoan.setText(String.valueOf(total));
    }

